public class Main1015 {
    public static void main(String[] args) {
        Scanner cin = new Scanner(System.in);
        while (cin.hasNext()) {
            String s = cin.nextLine();
            System.out.println(s.toLowerCase());
        }
        cin.close();
    }
}
